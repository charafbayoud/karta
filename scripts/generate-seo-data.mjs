function buildCatalog(source, region) {
  const seen = new Set();
  const cities = [];

  for (const [name, country, lat, lng] of source) {
    const slug = slugify(name);
    if (seen.has(slug)) continue;
    seen.add(slug);
    cities.push({ slug, name, country, region, lat, lng });
  }

  return cities;
}

const europeCities = buildCatalog(europe, "Europe");
const usCities = buildCatalog(unitedStates, "United States");
const combined = [...europeCities, ...usCities];
const unique = [];
const globalSeen = new Set();

for (const city of combined) {
  if (globalSeen.has(city.slug)) continue;
  globalSeen.add(city.slug);
  unique.push(city);
}

const SEO_CITIES = unique.slice(0, 300);

if (SEO_CITIES.length < 300) {
  console.error(`Expected 300 cities, got ${SEO_CITIES.length}`);
  process.exit(1);
}

